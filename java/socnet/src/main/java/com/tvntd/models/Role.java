package com.tvntd.models;

import java.util.Collection;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.JoinTable;
import javax.persistence.ManyToMany;

@Entity
public class Role
{
    public static final Long userIdStart = 1000L;

    public static String User    = "User";
    public static String Dba     = "Dba";
    public static String Admin   = "Admin";
    public static String Banker  = "Banker";
    public static String Auditor = "Auditor";
    public static String AuthUser   = "ROLE_" + User;
    public static String AuthDba    = "ROLE_" + Dba;
    public static String AuthAdmin  = "ROLE_" + Admin;
    public static String AuthBanker = "ROLE_" + Banker;

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;

    @ManyToMany(mappedBy = "roles")
    private Collection<User> users;

    @ManyToMany(fetch = FetchType.EAGER)
    @JoinTable(name = "RolesPrivileges",
        joinColumns = @JoinColumn(name = "RoleId", referencedColumnName = "id"),
        inverseJoinColumns = @JoinColumn(name = "PrivId",
            referencedColumnName = "id"))
    private Collection<Privilege> privileges;

    @Column(length = 16)
    private String name;

    public Role() {
        super();
    }

    public Role(final String name)
    {
        super();
        this.name = name;
    }

    //
    public Long getId() {
        return id;
    }

    public void setId(final Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public Collection<User> getUsers() {
        return users;
    }

    public void setUsers(final Collection<User> users) {
        this.users = users;
    }

    public Collection<Privilege> getPrivileges() {
        return privileges;
    }

    public void setPrivileges(final Collection<Privilege> privileges) {
        this.privileges = privileges;
    }

    @Override
    public int hashCode()
    {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        return result;
    }

    @Override
    public boolean equals(final Object obj)
    {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Role role = (Role) obj;
        if (!role.equals(role.name)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString()
    {
        StringBuilder sb = new StringBuilder();
        sb.append("Role name: ")
            .append(name)
            .append(", id: ")
            .append(id);
        return sb.toString();
    }
}
